// write a switch statement to print a month in quarter 
//  - month in quarter 1:january,February, March
//  - month in quarter 2:April, May, June
//  - month in quarter 3:July, August . september
//  - month in quarter 4:octomber,november, december

let num = 3;
switch (num) {
    case 1:
        console.log("January", "February", "march");
        break;

    case 2:
        console.log("April", "May", "June");
        break;

    case 3:
        console.log("July", "August", "September");
        break;
    case 4:
        console.log("Octomber", "November", "December");
        break;
    default:
        console("wrong choice");



}